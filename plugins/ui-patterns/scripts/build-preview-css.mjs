import { createRequire } from "node:module";
import { access, readFile, writeFile } from "node:fs/promises";
import { dirname, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { compile } from "@tailwindcss/node";
import { Scanner } from "@tailwindcss/oxide";

const scriptDirectory = dirname(fileURLToPath(import.meta.url));
const packageRoot = resolve(scriptDirectory, "..");
const require = createRequire(import.meta.url);

async function resolveDependencyFile(relativePath) {
  let directory = packageRoot;
  while (true) {
    const candidate = resolve(directory, "node_modules", relativePath);
    try {
      await access(candidate);
      return candidate;
    } catch {
      const parent = dirname(directory);
      if (parent === directory) {
        throw new Error(`Cannot resolve dependency file: ${relativePath}`);
      }
      directory = parent;
    }
  }
}

const twAnimateCssPath = await resolveDependencyFile(
  "tw-animate-css/dist/tw-animate.css",
);

const [globalsCss, shadcnTailwindCss, styleNovaCss] = await Promise.all([
  readFile(resolve(packageRoot, "vendor/shadcn-ui/styles/globals.css"), "utf8"),
  readFile(resolve(packageRoot, "vendor/shadcn-ui/styles/tailwind.css"), "utf8"),
  readFile(resolve(packageRoot, "vendor/shadcn-ui/styles/style-nova.css"), "utf8"),
]);

// The upstream globals file imports the whole shadcn docs application. The
// preview document needs the file's exact theme, variants, and base rules, but
// not those app-only imports or source-discovery directives. Tailwind itself,
// animations, and the pinned shadcn extension are supplied explicitly below.
const isolatedGlobalsCss = globalsCss
  .split("\n")
  .filter((line) => !line.startsWith("@import ") && !line.startsWith("@source "))
  .join("\n");

const input = [
  '@import "tailwindcss";',
  '@import "tw-animate-css";',
  shadcnTailwindCss,
  isolatedGlobalsCss,
  styleNovaCss,
  `@layer base {
    html, body, #ui-patterns-preview-root {
      min-width: 100%;
      min-height: 100%;
    }
    html {
      background: var(--background);
      color: var(--foreground);
    }
    body {
      min-height: 100vh;
      overflow: auto;
      background: var(--background);
      color: var(--foreground);
    }
    #ui-patterns-preview-root {
      box-sizing: border-box;
      display: grid;
      place-items: center;
      width: 100%;
      min-height: 100vh;
      padding: clamp(1rem, 5vw, 3rem);
      overflow: auto;
    }
    #ui-patterns-preview-root > * {
      min-width: 0;
      max-width: 100%;
    }
  }`,
  "",
].join("\n");

const compiler = await compile(input, {
  base: packageRoot,
  onDependency: () => {},
  customCssResolver: async (id, base) => {
    if (id === "tailwindcss") {
      return require.resolve("tailwindcss/index.css");
    }
    if (id === "tw-animate-css") {
      return twAnimateCssPath;
    }
    try {
      return require.resolve(id, { paths: [base, packageRoot] });
    } catch {
      return undefined;
    }
  },
});

const scanner = new Scanner({
  sources: [
    { base: packageRoot, pattern: "**/*", negated: false },
    { base: resolve(packageRoot, "dist"), pattern: "**/*", negated: true },
    { base: resolve(packageRoot, "node_modules"), pattern: "**/*", negated: true },
    {
      base: resolve(packageRoot, "generated"),
      pattern: "preview-css.ts",
      negated: true,
    },
  ],
});
const css = compiler.build(scanner.scan());

for (const requiredFragment of [
  ".style-nova",
  ".cn-button-variant-default",
  "[data-open]",
]) {
  if (!css.includes(requiredFragment)) {
    throw new Error(`preview CSS is missing ${requiredFragment}`);
  }
}

// Base64 keeps the generated CSS opaque to bb plugin build's own Tailwind
// source scanner while still bundling one deterministic, offline stylesheet
// for the isolated preview iframe.
const output = [
  "// Generated by scripts/build-preview-css.mjs — do not edit.",
  "// Sources: the pinned shadcn/ui theme, Tailwind extension, Nova style,",
  "// and the exact utility candidates used by the approved-source previews.",
  `export const PREVIEW_CSS = globalThis.atob(${JSON.stringify(Buffer.from(css).toString("base64"))});`,
  "",
].join("\n");
const outputPath = resolve(packageRoot, "generated/preview-css.ts");
await writeFile(outputPath, output);
console.log(`wrote ${relative(packageRoot, outputPath)} (${css.length} CSS bytes)`);
